    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String name = nameTextField.getText();
        String speciality = specialityTextField.getText();
        String gender = "";

        for (Enumeration<AbstractButton> buttons = genderRadioButtonGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                gender = button.getText();
                break;
            }
        }
        String hospital = hospitalComboBox.getSelectedItem().toString();

        if (name.isBlank() || speciality.isBlank() || gender.isBlank() || hospital.isBlank()) {
            JOptionPane.showMessageDialog(rootPane, "Tolong isi semua form", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            Connection connection = DatabaseConnection.getConnection();
            String query = "INSERT INTO doctors(name, speciality, gender, hospital) VALUES(?, ?, ?, ?)";
            try (PreparedStatement statement = connection.prepareStatement(query)) {
                statement.setString(1, name);
                statement.setString(2, speciality);
                statement.setString(3, gender);
                statement.setString(4, hospital);

                statement.executeUpdate();
            }

            nameTextField.setText("");
            specialityTextField.setText("");
            genderRadioButtonGroup.clearSelection();
            hospitalComboBox.setSelectedIndex(0);

            JOptionPane.showMessageDialog(rootPane, "Data berhasil disimpan", "Info", JOptionPane.INFORMATION_MESSAGE);

            fetchData();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addButtonActionPerformed

