    private void fetchData() {
        String name, speciality, gender, hospital;

        try {
            doctorModel.setRowCount(0);

            Connection connection = DatabaseConnection.getConnection();
            if (connection == null) {
                return;
            }

            Statement statement = connection.createStatement();
            String query = "SELECT name, speciality, gender, hospital FROM doctors";
            ResultSet resultSet = statement.executeQuery(query);

            while (resultSet.next()) {
                name = resultSet.getString("name");
                speciality = resultSet.getString("speciality");
                gender = resultSet.getString("gender");
                hospital = resultSet.getString("hospital");
                Object[] data = {name, speciality, gender, hospital};

                doctorModel.addRow(data);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

